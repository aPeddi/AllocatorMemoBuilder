def render_html(memo: Memo) -> str:
    e = html.escape
    a = memo.audit
    rows = []
    for s in memo.shortlist:
        m = s.metrics
        rows.append(
            f"<tr><td>{s.rank}</td><td>{e(s.name)}</td><td>{e(s.strategy)}</td>"
            f"<td class='mono'>{_pct(m.get('ann_return'))}</td><td class='mono'>{_num(m.get('sharpe'))}</td>"
            f"<td class='mono'>{_num(m.get('sortino'))}</td><td class='mono'>{_num(m.get('calmar'))}</td>"
            f"<td class='mono'>{_pct(m.get('max_drawdown'))}</td></tr>"
        )
    cards = []
    for sec in memo.sections:
        chips = ""
        if sec.claims:
            cs = []
            for c in sec.claims:
                cls = "ok" if c.verified else "warn"
                src = e("; ".join(c.source_refs) or "no source")
                cs.append(f"<span class='chip {cls}' title='{src}'>{'✓' if c.verified else '⚠'} {e(c.text)}</span>")
            chips = f"<div class='claims'>{''.join(cs)}</div>"
        cards.append(f"<div class='card'><h2>{e(sec.heading)}</h2><div>{e(sec.body)}</div>{chips}</div>")
    return (
        f"<!doctype html><html><head><meta charset='utf-8'><title>{e(memo.title)}</title>"
        f"<style>{_CSS}</style></head><body><div class='wrap'>"
        f"<h1>{e(memo.title)}</h1>"
        f"<div class='meta'>Mandate: {e(memo.mandate)} &nbsp;·&nbsp; Generated by <span class='mono'>{e(memo.generated_by)}</span> &nbsp;·&nbsp; {e(memo.version)}</div>"
        f"<div class='meta'><span class='badge'>{a.get('verified_count',0)}/{a.get('claim_count',0)} claims verified</span></div>"
        f"<table><thead><tr><th>#</th><th>Fund</th><th>Strategy</th><th>Ann.Return</th><th>Sharpe</th><th>Sortino</th><th>Calmar</th><th>MaxDD</th></tr></thead>"
        f"<tbody>{''.join(rows)}</tbody></table>"
        f"{''.join(cards)}"
        f"<div class='foot'>AllocatorMemoBuilder · every figure traces to a source row + formula · print to PDF from your browser</div>"
        f"</div></body></html>"
    )


def write_html(memo: Memo, path: str | Path) -> Path:
    p = Path(path)
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(render_html(memo))
    return p


def write_xlsx(memo: Memo, ctx, path: str | Path) -> Path:
    from openpyxl import Workbook
    from openpyxl.styles import Font, PatternFill

    p = Path(path)
    p.parent.mkdir(parents=True, exist_ok=True)
    wb = Workbook()
    head_fill = PatternFill("solid", fgColor="0A1628")
    head_font = Font(bold=True, color="00E5C4")

    def style_header(ws, ncols):
        for c in range(1, ncols + 1):
            cell = ws.cell(row=1, column=c)
            cell.fill = head_fill
            cell.font = head_font

    # Shortlist
    ws = wb.active
    ws.title = "Shortlist"
    cols = ["rank", "fund_id", "name", "strategy", "score", "ann_return", "sharpe", "sortino", "calmar", "max_drawdown", "ann_vol"]
    ws.append([c.replace("_", " ").title() for c in cols])
    for s in memo.shortlist:
        m = s.metrics
        ws.append([s.rank, s.fund_id, s.name, s.strategy, s.score,
                   m.get("ann_return"), m.get("sharpe"), m.get("sortino"),
                   m.get("calmar"), m.get("max_drawdown"), m.get("ann_vol")])
    style_header(ws, len(cols))

    # All-funds metrics
    ws2 = wb.create_sheet("Metrics")
    hdr = ["fund_id", "name"] + METRIC_KEYS
    ws2.append([h.replace("_", " ").title() for h in hdr])
    for fid, vals in ctx.metrics_by_fund.items():
        f = ctx.get_fund(fid)
        ws2.append([fid, f.name if f else fid] + [vals.get(k) for k in METRIC_KEYS])
    style_header(ws2, len(hdr))
    for ci, k in enumerate(hdr):
        if k in _PCT:
            for r in range(2, ws2.max_row + 1):
                ws2.cell(row=r, column=ci + 1).number_format = "0.0%"

    # Audit trail
    ws3 = wb.create_sheet("Audit")
    ws3.append(["fund_id", "metric", "value", "verified", "sources"])
    for c in memo.audit.get("claims", []):
        ws3.append([c.get("fund_id"), c.get("metric"), c.get("value"), c.get("verified"), "; ".join(c.get("sources", []))])
    style_header(ws3, 5)

    wb.save(p)
    return p


def export_all(memo: Memo, ctx=None, outdir: str | Path = "exports") -> dict:
    paths = {
        "md": write_markdown(memo, Path(outdir) / "memo.md"),
        "json": write_json(memo, Path(outdir) / "memo.json"),
        "html": write_html(memo, Path(outdir) / "memo.html"),
    }
    if ctx is not None:
        paths["xlsx"] = write_xlsx(memo, ctx, Path(outdir) / "memo.xlsx")
    return paths
