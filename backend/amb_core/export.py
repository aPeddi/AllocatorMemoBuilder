"""Export a memo to Markdown, JSON, styled HTML, and XLSX.

Local-first product surface (ADR-0009): the deliverables are files, not a web
app. HTML is self-contained and print-to-PDF ready in any browser.
"""
from __future__ import annotations

import html
from pathlib import Path

from .memo import render_markdown
from .metrics import METRIC_KEYS
from .models import Memo

# metrics that are fractions (render as percentages)
_PCT = {"ann_return", "ann_vol", "max_drawdown", "downside_dev", "alpha", "tracking_error", "hit_rate"}


def _pct(x):
    return "n/a" if x is None else f"{x * 100:.1f}%"


def _num(x):
    return "n/a" if x is None else f"{x:.2f}"


def write_markdown(memo: Memo, path: str | Path) -> Path:
    p = Path(path)
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(render_markdown(memo))
    return p


def write_json(memo: Memo, path: str | Path) -> Path:
    p = Path(path)
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(memo.model_dump_json(indent=2))
    return p


